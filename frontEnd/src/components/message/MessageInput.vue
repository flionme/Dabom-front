<script setup>
import { ref, nextTick } from 'vue';
import { useSocketStore } from '@/stores/socket';

const messageText = ref('');
const textarea = ref(null);
const emit = defineEmits(['send-message', 'attach', 'image', 'voice']);

const socketStore = useSocketStore(); // 스토어 사용

function handleInput() {
  if (textarea.value) {
    textarea.value.style.height = 'auto';
    textarea.value.style.height = Math.min(textarea.value.scrollHeight, 120) + 'px';
  }
}

function handleKeydown(e) {
  if (e.key === 'Enter' && !e.shiftKey && messageText.value.trim()) {
    e.preventDefault();
    sendMessage();
  }
}

function sendMessage() {
  if (!messageText.value.trim()) return;

  const roomIdx = localStorage.getItem('currentChatId');
  const recipientIdx = localStorage.getItem('recipientIdx');

  const messageDto = {
    roomIdx: parseInt(roomIdx),
    recipientIdx: parseInt(recipientIdx),
    message: messageText.value.trim(),
  };

  socketStore.sendMessage(messageDto); // 스토어를 통해 메시지 전송

  messageText.value = '';
  nextTick(() => {
    if (textarea.value) {
      textarea.value.style.height = 'auto';
    }
  });
}

function handleAttach() {
  emit('attach');
}

function handleImage() {
  emit('image');
}

function handleVoice() {
  emit('voice');
}

</script>

<template>
  <div class="message-input-container">
    <div class="message-input-wrapper">
      <div class="input-actions">
        <button class="input-btn" @click="handleAttach">
          <i class="fas fa-plus"></i>
        </button>
        <button class="input-btn" @click="handleImage">
          <i class="fas fa-image"></i>
        </button>
        <button class="input-btn" @click="handleVoice">
          <i class="fas fa-microphone"></i>
        </button>
      </div>
      <div class="message-input-area">
        <textarea
          class="message-input"
          placeholder="메시지 입력..."
          rows="1"
          v-model="messageText"
          @input="handleInput"
          @keydown="handleKeydown"
          ref="textarea"
        ></textarea>
        <button
          class="send-btn"
          :disabled="!messageText.trim()"
          @click="sendMessage"
        >
          <i class="fas fa-paper-plane"></i>
        </button>
      </div>
    </div>
  </div>
</template>

